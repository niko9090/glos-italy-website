// Type definitions for type-safe translations
export type Locale = keyof typeof translations;
export type TranslationKeys = typeof translations.it;

// Helper function to get nested translation value
export function getTranslation<T extends keyof TranslationKeys>(
  locale: Locale,
  section: T
): TranslationKeys[T] {
  return translations[locale][section] as TranslationKeys[T];
}

// Helper function to replace placeholders in translation strings
export function formatTranslation(
  text: string,
  replacements: Record<string, string | number>
): string {
  return Object.entries(replacements).reduce(
    (result, [key, value]) => result.replace(`{${key}}`, String(value)),
    text
  );
}
